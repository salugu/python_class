# arithmetic operators
a = 10
b = 3   
print("Addition:", a + b)         
print("Subtraction:", a - b)       
print("Multiplication:", a * b)    
print("Division:", a / b)    
print("Floor Division:", a // b)
print("Modulus:", a % b)    
print("Exponentiation:", a ** b)
# comparison operators
a=10
b=20
print(a == b)
print(a != b)
print(a is b)
print(a is not b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
# logical operators
a=5
b=12
print(a < 10 and b > 10)
print(a < 10 or b < 10)
print(not(a < 10 and b > 10))

# bitwise operators
a=10
b=20
print(a & b)  
print(a | b)  
print(a ^ b)    
